package core;

import java.awt.event.KeyEvent;
import java.awt.event.KeyListener;

/**
 *
 * The intention of this class is to support the user's hot-key shortcuts. Interviews with photographers show that some
 * photographers, particularly older users, have indicated that these keys are really useful.
 */
class HotKeyListener implements KeyListener{

    @Override
    public void keyTyped(KeyEvent e) {
    }

    @Override
    public void keyPressed(KeyEvent e) {

    }

    @Override
    public void keyReleased(KeyEvent e) {

    }
}